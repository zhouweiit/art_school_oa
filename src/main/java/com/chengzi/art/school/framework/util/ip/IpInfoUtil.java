    public static String getFullIpInfo(String ip) {
        try {
            String[] ipInfos = IPExt.find(ip);
            if ("中国".equals(ipInfos[0])) {
                if (isps.containsKey(ipInfos[4]))
                    ipInfos[4] = isps.get(ipInfos[4]);
                return ipInfos[1] + ipInfos[2] + ipInfos[4];
            } else {
                return ipInfos[0] + ipInfos[1];
            }
        } catch (Exception e) {
            return ip;
        }
    }

	public static String getFullIpInfoByDistinct(String ip) {
		try {
			String[] ipInfos = IPExt.find(ip);
			if ("中国".equals(ipInfos[0])) {
				if (isps.containsKey(ipInfos[4]))
					ipInfos[4] = isps.get(ipInfos[4]);
				if (isRepeatable(ipInfos[1], ipInfos[2])) {
					return ipInfos[1] + ipInfos[4];
				}else {
					return ipInfos[1] + ipInfos[2] + ipInfos[4];
				}
			} else {
				return ipInfos[0] + ipInfos[1];
			}
		} catch (Exception e) {
			return ip;
		}
	}

	private static boolean isRepeatable(String province, String city) {
		if ("吉林".equals(province) && "吉林".equals(city)) {
			return false;
		}
		return province.equals(city) ? true : false;
	}
    
