    public static String getSampleIpInfo(String ip) {
        try {
            String[] ipInfos = IPExt.find(ip);
            if ("中国".equals(ipInfos[0])) {
                if (isps.containsKey(ipInfos[4]))
                    ipInfos[4] = isps.get(ipInfos[4]);
                return ipInfos[1] + ipInfos[4];
            } else {
                return ipInfos[0];
            }
        } catch (Exception e) {
            return ip;
        }
    }
    
